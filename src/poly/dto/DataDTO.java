package poly.dto;

public class DataDTO {
	private String analysis_time;
	private String commu_name;
	private String word;
	private String count;
	
	public String getAnalysis_time() {
		return analysis_time;
	}
	public void setAnalysis_time(String analysis_time) {
		this.analysis_time = analysis_time;
	}
	public String getCommu_name() {
		return commu_name;
	}
	public void setCommu_name(String commu_name) {
		this.commu_name = commu_name;
	}
	public String getWord() {
		return word;
	}
	public void setWord(String word) {
		this.word = word;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	
	
}
